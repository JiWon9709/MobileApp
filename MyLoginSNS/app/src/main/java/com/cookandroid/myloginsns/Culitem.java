package com.cookandroid.myloginsns;

public class Culitem {
    private String host ;
    private String info ;
    private String location;
    private String tel;
    private String title;

    public void sethost(String title) {
        host = title ;
    }
    public void setinfo(String title) {
        info = title ;
    }
    public void setlocation(String title) {
        location = title ;
    }
    public void settel(String title) {
        tel = title ;
    }
    public void settitle(String t) {
        title = t ;
    }

    public String gethost() {
        return this.host ;
    }
    public String getinfo() {
        return this.info ;
    }
    public String getlocation() {
        return this.location ;
    }
    public String gettel() {
        return this.tel ;
    }
    public String gettitle() {
        return this.title ;
    }
}

