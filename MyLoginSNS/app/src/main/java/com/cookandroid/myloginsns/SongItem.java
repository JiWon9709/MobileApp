package com.cookandroid.myloginsns;


public class SongItem {
    private String gpsX, min, sec, con;

    public void setGpsX(String title) {
        gpsX = title ;
    }

    public String getGpsX() {
        return this.gpsX ;
    }
    public void setMin(String minute) {
        min = minute ;
    }
    public String getMin() {
        return this.min ;
    }

    public void setSec(String second) {
        sec = second ;
    }

    public String getSec() {
        return this.sec ;
    }

    public void setCon(String contents) {
        con = contents ;
    }

    public String getCon() {
        return this.con ;
    }


}
